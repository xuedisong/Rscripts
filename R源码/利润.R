f<-function(e){     ##e为利润,单位为万
  print('单位为“万”')
  if(e<=10)r<-0.1*e else
    if(e>10&e<=20){
      r<-0.1*10+(e-10)*0.075
    }else
      if(e>20&e<=40){
        r<-0.1*10+0.075*10+0.05*(e-20)
      }else
        if(e>40&e<=60){
          r<-0.1*10+0.075*10+0.05*20+0.03*(e-40)
        }else
          if(e>=60&e<100){
            r<-0.1*10+0.075*10+0.05*20+0.03*20+0.015*(e-60)
          }else{
            r<-0.1*10+0.075*10+0.05*20+0.03*20+0.015*40+0.01*(e-100)
          }
  return(r)
}